package com.class10;

public class AllElementsFromAnArray {

	public static void main(String[] args) {
		
		//[]=indexes
		
		System.out.println("\n\t________________print in increment i++ ________________\n");

		
		int []numbersa= {10,14,78,5,90,76};
		
		for(int i=0;i<numbersa.length;i++) {
		
		
		System.out.print(numbersa[i]+" ");
		//print in reverse}
		}
		System.out.println("\n\n\t____________print in reverse/decrement i-- ____________\n");

		int []numbers= {10,14,78,5,90,76,1,8};
		
		for(int i=numbers.length-1;i>=0;i--)
		
		
		System.out.print(numbers[i]+" ");
		

	}

}
